# Data Type

# String
a = str("Hello World") 
# Integer
b = int(20)
# Float
c = float(20.5)
# Complex
d = complex(1j)
# List
e = list(["apple", "banana", "cherry"])
# Tuple
f = tuple(("apple", "banana", "cherry"))
# Range
g = range(6)
# Dictionary
h = dict({"name": "John", "age": 36})
# Set
i = set({"apple", "banana", "cherry"})
# Frozen Set
j = frozenset(("apple", "banana", "cherry"))
# Boolean
k = bool(5)
# Bytes
l = bytes(5)
# Bytearray
m = bytearray(5)
# Memoryview
n = memoryview(bytes(5))

